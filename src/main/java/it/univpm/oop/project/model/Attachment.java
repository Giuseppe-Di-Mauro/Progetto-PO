package it.univpm.oop.project.model;

/**
 * The Class Attachment.
 */
public class Attachment {
	
	/** The media. */
	private Media media;
	
	/** The type. */
	private String type;
	
	/** The url. */
	private String url;
	
	/**
	 * Gets the media.
	 *
	 * @return the media
	 */
	public Media getMedia() {
		return media;
	}
	
	/**
	 * Sets the media.
	 *
	 * @param media the new media
	 */
	public void setMedia(Media media) {
		this.media = media;
	}
	
	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	
	/**
	 * Sets the type.
	 *
	 * @param type the new type
	 */
	public void setType(String type) {
		this.type = type;
	}
	
	/**
	 * Gets the url.
	 *
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	
	/**
	 * Sets the url.
	 *
	 * @param url the new url
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	
}
